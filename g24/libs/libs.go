package libs

import (
	"math/rand"
	"strconv"
	"time"
)

////////////////////////////////////////////////////////////////////////////
// Function definitions

func Int2Str(i int) string {
	return strconv.Itoa(i)
}

func RandI() {
	rand.Seed(time.Now().Unix())
}

func RandN(i int) int {
	return rand.Intn(i)
}
